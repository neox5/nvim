opt.tabstop = 2

-- Search settings
opt.ignorecase = true
opt.smartcase = true

-- Appearance
opt.termguicolors = true
opt.background = "dark"

-- Clipboard
opt.clipboard = "unnamedplus"

-- Debugging
opt.showcmd = true
